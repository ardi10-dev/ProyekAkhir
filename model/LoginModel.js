class LoginModel {
    constructor(id, username, password, role, nama, nip, staff, imageUrl ){
        this.id = id;
        this.username = username;
        this.password = password;
        this.role = role;
        this.nama = nama;
        this.nip = nip;
        this.staff = staff;
        this.imageUrl = imageUrl;
    }
}

export default LoginModel;